export const questionBank: Question[] = Object.entries(levelFacts).flatMap(([level, facts]) =>
  facts.flatMap((item, index) => [
    ...buildQuestionSet(Number(level), item, index),
    ...buildTrueFalseSet(Number(level), item, index),
    createFillBlankQuestion(Number(level), item, index),
    createOrderingQuestion(Number(level), facts, index),
  ]),
);

function buildQuestionSet(level: number, fact: Fact, index: number): MultipleChoiceQuestion[] {
  const baseId = `L${level}-${String(index + 1).padStart(2, "0")}`;
  const reference = levelReferences[level][index];
  return [
    createQuestion(baseId, level, `${fact.clue}은(는) ${getClueQuestionWord(fact.clue)}?`, fact, reference, 0),
    createQuestion(baseId, level, `${fact.topic}${getAndParticle(fact.topic)} 가장 직접 관련된 설명은 무엇입니까?`, fact, reference, 1),
    createQuestion(baseId, level, `다음 중 ${fact.topic}에 대한 바른 답은 무엇입니까?`, fact, reference, 2),
  ];
}

function buildTrueFalseSet(level: number, fact: Fact, index: number): TrueFalseQuestion[] {
  const baseId = `L${level}-${String(index + 1).padStart(2, "0")}-tf`;
  const reference = levelReferences[level][index];
  return [
    createTrueFalseQuestion(baseId, level, `${fact.topic}: ${fact.answer}`, true, fact, reference, 0),
    createTrueFalseQuestion(baseId, level, `${fact.topic}: ${fact.wrong[0]}`, false, fact, reference, 1),
  ];
}

function getClueQuestionWord(clue: string) {
  return isPersonClue(clue) ? "누구입니까" : "무엇입니까";
}

function isPersonClue(clue: string) {
  if (/핵심|중심|교훈|내용|결과|의미|주제|표징|행동|기간|장소|재료|직업/.test(clue)) return false;

  return /분|사람|사람들|인물|인물들|왕|여인|여인들|선지자|사사|제자|종들|무리/.test(clue);
}

function getAndParticle(value: string) {
  const lastChar = value.trim().at(-1);
  if (!lastChar) return "과(와)";

  const code = lastChar.charCodeAt(0);
  const hangulStart = "가".charCodeAt(0);
  const hangulEnd = "힣".charCodeAt(0);
  if (code < hangulStart || code > hangulEnd) return "과(와)";

  return (code - hangulStart) % 28 === 0 ? "와" : "과";
}

function createQuestion(
  baseId: string,
  level: number,
  question: string,
  fact: Fact,
  reference: string,
  variant: number,
): MultipleChoiceQuestion {
  const options = rotate<[string, string, string, string]>([fact.answer, ...fact.wrong], variant);
  return {
    id: `${baseId}-${variant + 1}`,
    level,
    type: "multiple_choice",
    category: fact.topic,
    difficulty: getDifficulty(level),
    question,
    payload: {
      choices: options,
    },
    answer: {
      index: options.indexOf(fact.answer),
    },
    explanation: fact.explanation,
    reference,
  };
}

function createTrueFalseQuestion(
  baseId: string,
  level: number,
  statement: string,
  answer: boolean,
  fact: Fact,
  reference: string,
  variant: number,
): TrueFalseQuestion {
  return {
    id: `${baseId}-${variant + 1}`,
    level,
    type: "true_false",
    category: fact.topic,
    difficulty: getDifficulty(level),
    question: `${statement} - OX`,
    payload: {},
    answer: {
      value: answer,
    },
    explanation: fact.explanation,
    reference,
  };
}

function createFillBlankQuestion(level: number, fact: Fact, index: number): FillBlankQuestion {
  return {
    id: `L${level}-${String(index + 1).padStart(2, "0")}-blank-1`,
    level,
    type: "fill_blank",
    category: fact.topic,
    difficulty: getDifficulty(level),
    question: `${fact.clue}: ______`,
    payload: {
      blankLabel: fact.topic,
    },
    answer: {
      text: fact.answer,
    },
    explanation: fact.explanation,
    reference: levelReferences[level][index],
  };
}

function createOrderingQuestion(level: number, facts: Fact[], index: number): OrderingQuestion {
  const windowSize = 4;
  const startIndex = Math.min(index, Math.max(0, facts.length - windowSize));
  const orderedFacts = facts.slice(startIndex, startIndex + windowSize);
  const orderedItems = orderedFacts.map((item) => item.topic);

  return {
    id: `L${level}-${String(index + 1).padStart(2, "0")}-order-1`,
    level,
    type: "ordering",
    category: orderedItems[0],
    difficulty: getDifficulty(level),
    question: "Arrange these Bible events in the correct order.",
    payload: {
      items: rotate(orderedItems, (index % (windowSize - 1)) + 1),
    },
    answer: {
      order: orderedItems,
    },
    explanation: `The event flow is: ${orderedItems.join(" > ")}.`,
    reference: orderedFacts.map((item) => levelReferences[level][facts.indexOf(item)]).join(", "),
  };
}

function getDifficulty(level: number): Difficulty {
  if (level <= 3) return "easy";
  if (level <= 6) return "medium";
  if (level <= 8) return "hard";
  return "expert";
}

function rotate<T extends unknown[]>(items: T, amount: number): T {
  return [...items.slice(amount), ...items.slice(0, amount)] as T;
}

function fact(topic: string, answer: string, wrong: [string, string, string], clue: string, explanation: string): Fact {
  return { topic, answer, wrong, clue, explanation };
}
