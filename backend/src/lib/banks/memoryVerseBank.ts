export const memoryVerseQuestions: MemoryVerseQuestion[] = rawVerses.map((verse, index) => {
  const level = Math.floor(index / QUESTIONS_PER_RUN) + 1;
  const { segments, blanks } = parseVerse(verse.text);

  return {
    id: questionId(level, "memory", index),
    level,
    type: "memory_verse",
    category: verse.title,
    difficulty: getDifficulty(level),
    question: `${verse.reference} 말씀을 순서대로 채우세요`,
    explanation: `${verse.reference} 말씀입니다.`,
    reference: verse.reference,
    payload: { segments },
    answer: { blanks },
  };
});

if (memoryVerseQuestions.length !== MEMORY_VERSE_LEVELS * QUESTIONS_PER_RUN) {
  throw new Error(
    `Memory verse bank expected ${MEMORY_VERSE_LEVELS * QUESTIONS_PER_RUN} verses but got ${memoryVerseQuestions.length}`,
  );
}
